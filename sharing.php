<div class="tableSubTitle">How do I link to a list of KamTape videos?</div>

<span class="standoutLabel">
Want to show off more than one of your own KamTape videos, or specifically tagged videos, on your website? Below are a few options! 
</span>
<br />
<br />
<br />
By placing a small snippet of HTML code in your webpage, you can pull up a list of all your KamTape videos in a neat, little window. Take a look at the example below; on the left is the HTML snippet that you would copy+paste into your webpage.
Remember to replace the part of the code that says &quot;YOUR_USERNAME&quot; with your KamTape username. 
 As a result, a small box with your videos will be rendered as shown on the right.

